<h5 style="font-size:150%">Investigate patterns of association in bivariate data</h5>
<ul style="font-size:125%">
<li>Construct and interpret scatter plots for bivariate measurement data to investigate patterns of association between two quantities. Describe patterns such as <span style="color:lightgray;">clustering, outliers, positive or negative association,</span> linear association, and nonlinear association.</li>
<li>Know that straight lines are widely used to model relationships between two quantitative variables. For scatter plots that suggest a linear association<span style="color:lightgray;">, informally fit a straight line, and informally assess the model fit by judging the closeness of the data points to the line</span>.  (CCSS-M, p. 56)</li>
</ul>

